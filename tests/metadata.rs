#[test]
fn artwork_invalid() {
  #[track_caller]
  fn case(filename: &str, bytes: impl AsRef<[u8]>, stderr: &str) {
    Test::new()
      .write(filename, bytes)
      .write("metadata.yaml", format!("title: Foo\nartwork: {filename}"))
      .arg("create")
      .stderr_regex(stderr)
      .failure();
  }

  case(
    "cover.jpg",
    b"bar",
    "error: failed to decode JPEG artwork `.*cover\\.jpg`\n.*",
  );
  case(
    "cover.png",
    b"bar",
    "error: failed to decode PNG artwork `.*cover\\.png`\n.*",
  );
  case(
    "cover.jpg",
    png_bytes(1, 1),
    "error: failed to decode JPEG artwork `.*cover\\.jpg`\n.*",
  );
  case(
    "cover.png",
    jpeg_bytes(1),
    "error: failed to decode PNG artwork `.*cover\\.png`\n.*",
  );
  case(
    "cover.jpg",
    jpeg_bytes(2),
    "error: artwork `.*cover\\.jpg` is 2x1, but must be square\n",
  );
  case(
    "cover.png",
    png_bytes(2, 1),
    "error: artwork `.*cover\\.png` is 2x1, but must be square\n",
  );
}

